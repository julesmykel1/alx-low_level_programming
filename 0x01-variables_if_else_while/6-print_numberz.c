#include <stdio.h>

/**
 * main - Entry a number to the variable n and print character.
 *
 * Description: Prints the alphabet in lowercase.
 *
 * Return: Always 0 (Success)
 */
int main(void)
  
{
  int c;
  for (c = '0' ; c <= '9' ; c++)
    {
      putchar(c); 
    }
  putchar('\n');
  return (0); 
}
